package ArrayListExercise.Second;

public class Gamer {
    private String name;
    private int numberOfGamer;

    public Gamer(String name, int numberOfGamer) {
        this.name = name;
        this.numberOfGamer = numberOfGamer;
    }

    public String getName() {
        return name;
    }

    public int getNumberOfGamer() {
        return numberOfGamer;
    }


}
